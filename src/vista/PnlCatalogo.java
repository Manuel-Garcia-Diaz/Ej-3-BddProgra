    // Metodo para cargar las categorias en el ComboBox al entrar a la pantalla
    public void actualizarDatos() {
        try {
            cmbCategorias.removeAllItems();
            List<Categoria> cats = db.obtenerCategorias();
            for (Categoria c : cats) {
                cmbCategorias.addItem(c.getDenominacion()); // Guardamos solo el nombre por simplicidad visual
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    // Metodo para llenar la tabla
    private void cargarTablaProductos() {
        int indexCat = cmbCategorias.getSelectedIndex();
        if (indexCat == -1) {
            return;
        }

        modeloTabla.setRowCount(0); // Limpiar tabla
        try {
            // Obtenemos las categorias para saber el ID de la seleccionada
            List<Categoria> cats = db.obtenerCategorias();
            int idCat = cats.get(indexCat).getId();

            List<Producto> prods = db.obtenerProductosPorCategoria(idCat);
            for (Producto p : prods) {
                modeloTabla.addRow(new Object[]{p.getId(), p.getDenominacion()});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

