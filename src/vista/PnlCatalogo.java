    private void btnAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProductoActionPerformed
        int indexCat = cmbCategorias.getSelectedIndex();
        String nom = txtNuevoProducto.getText().trim();
        if (indexCat != -1 && !nom.isEmpty()) {
            try {
                List<Categoria> cats = db.obtenerCategorias();
                int idCat = cats.get(indexCat).getId();

                db.insertarProducto(nom, idCat);
                txtNuevoProducto.setText("");
                cargarTablaProductos(); // Refresca la tabla
                javax.swing.JOptionPane.showMessageDialog(this, "Producto creado");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnAgregarProductoActionPerformed

