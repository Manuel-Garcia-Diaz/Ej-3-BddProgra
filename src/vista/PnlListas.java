    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int indexList = cmbListas.getSelectedIndex();
        int indexProd = cmbProd.getSelectedIndex();

        if (indexList == -1 || indexProd == -1) {
            return;
        }

        try {
            int idLista = cacheListas.get(indexList).getId();
            int idProd = cacheProds.get(indexProd).getId();
            int cant = Integer.parseInt(txtCantidad.getText());

            db.agregarProductoLista(idLista, idProd, cant);
            cargarTablaLista(); // Refrescar tabla

        } catch (java.sql.SQLIntegrityConstraintViolationException ex) {
            // CUMPLIENDO EL PDF: Evitar duplicados
            javax.swing.JOptionPane.showMessageDialog(this, "El producto ya existe en la lista.");
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed
