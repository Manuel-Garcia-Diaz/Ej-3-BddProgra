    public void actualizarDatosGlobales() {
        try {
            cmbListas.removeAllItems();
            cacheListas = db.obtenerListas();
            for (Lista l : cacheListas) {
                cmbListas.addItem(l.getDenominacion());
            }

            cmbCat.removeAllItems();
            cacheCats = db.obtenerCategorias();
            for (Categoria c : cacheCats) {
                cmbCat.addItem(c.getDenominacion());
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void cargarTablaLista() {
        int indexList = cmbListas.getSelectedIndex();
        modeloTabla.setRowCount(0);
        if (indexList == -1 || cacheListas == null) {
            return;
        }

        try {
            int idLista = cacheListas.get(indexList).getId();
            List<Object[]> prods = db.obtenerProductosDeLista(idLista);
            for (Object[] fila : prods) {
                modeloTabla.addRow(fila);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

